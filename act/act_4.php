	// tabs
	$(document).ready(function () {
		$('.tabs-type1 .current').click(function () {
      const tabs = $(this).closest('.tabs-type1');
      const list = tabs.find('.list');
      if (!tabs.hasClass('on')) {
        tabs.addClass('on');
        list.slideDown(200);
      } else {
        tabs.removeClass('on');
        list.slideUp(200);
      }
    });
		$(".tabs-type1 .list button").click(function () {
			var index = $(this).index();
			var buttonText = $(this).text();
			$(".tabs-type1 .list button").removeClass("active");
			$(this).addClass("active");
			$(".tab-container.type1 > .tab-content").hide();
			$(".tab-container.type1 > .tab-content").eq(index).show();
			$(this).closest('.tabs-type1').find('.current').text(buttonText);
			$('.tabs-type1').removeClass('on');
			if (window.matchMedia("(max-width: 768px)").matches) {
				$('.tabs-type1 .list').slideUp(200);
			}
		});
		
		$(".tabs-type3 button").click(function () {
			var index = $(this).index();
			$(".tabs-type3 button").removeClass("active");
			$(this).addClass("active");
			$(".tab-container.type3 > .tab-content").hide();
			$(".tab-container.type3 > .tab-content").eq(index).show();
		});

		$(".history-tab .tab-wrap button").click(function () {
			const index = $(".history-tab .tab-wrap button").index(this);
			$(".history-tab .tab-wrap button").removeClass("active");
			$(this).addClass("active");
			$(".history-tab + .tab-container .tab-content").hide();
			$(".history-tab + .tab-container .tab-content").eq(index).show();
		});
	});
