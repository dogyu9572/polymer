- 서비스를 통해 얻은 정보를 회사의 사전 승낙 없이 서비스 이용 외의 목적으로 복제하거나, 이를 출판 및 방송 등에 사용하거나, 제 3자에게 제공하는 행위<br />
- 회사의 운영진, 직원 또는 관계자를 사칭하거나 고의로 서비스를 방해하는 등 정상적인 서비스 운영에 방해가 될 경우<br />
- 정보통신 윤리위원회 등 관련 공공기관의 시정 요구가 있는 경우<br />
- 3개월 이상 서비스를 이용한 적이 없는 경우<br />
- 약관을 포함하여 회사가 정한 제반 규정을 위반하거나 범죄와 결부된다고 객관적으로 판단되는 등 제반 법령을 위반하는 행위<br /><br />

제 21 조 (손해배상)<br />
