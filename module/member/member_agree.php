- 불법복제 또는 해킹을 조장하는 내용인 경우<br />
- 영리를 목적으로 하는 광고일 경우<br />
- 범죄와 결부된다고 객관적으로 인정되는 내용일 경우<br />
- 다른 이용자 또는 제 3자의 저작권 등 기타 권리를 침해하는 내용인 경우<br />
