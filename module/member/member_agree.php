(10) 회원은 스토킹(stalking) 등 다른 이용자를 괴롭히는 행위를 해서는 안 된다.<br />
(11) 회원은 다른 이용자에 대한 개인정보를 그 동의 없이 수집, 저장, 공개하는 행위를 해서는 안 된다.<br /><br />

제 13 조 (서비스 이용 시간)<br />
